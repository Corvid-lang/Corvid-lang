fn render_types_rs(context: &BindingContext) -> String {
    let mut out = String::new();
    out.push_str("// @generated by corvid bind rust.\n");
    out.push_str(&format!(
        "// descriptor sha256: {}\n",
        context.descriptor_hash_hex
    ));
    out.push_str(&format!("// corvid-bind version: {}\n", context.bind_version));
    out.push_str(&format!(
        "// descriptor generated_at: {}\n\n",
        context.abi.generated_at
    ));
    out.push_str("use serde::{Deserialize, Serialize};\n\n");
    for ty in &context.abi.types {
        if ty.kind != "struct" {
            continue;
        }
        out.push_str("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]\n");
        out.push_str(&format!("pub struct {} {{\n", pascal_case(&ty.name)));
        for field in &ty.fields {
            out.push_str(&format!(
                "    pub {}: {},\n",
                snake_case(&field.name),
                rust_public_type(&field.r#type)
            ));
        }
        out.push_str("}\n\n");
    }
    out
}

