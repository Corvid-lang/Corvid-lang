use std::path::PathBuf;

use anyhow::Result;

use crate::{
    effective_return_ownership, grounded_inner_kind,
    is_generated_user_agent, pascal_case, python_ctypes_param_type, python_ctypes_return_type,
    python_public_type, python_string_literal, returns_grounded, snake_case, BindingContext,
    GeneratedFile,
};

pub(crate) fn render(context: &BindingContext) -> Result<Vec<GeneratedFile>> {
    let package_dir = PathBuf::from(&context.package_dir_name);
    let mut files = Vec::new();
    files.push(GeneratedFile {
        relative_path: PathBuf::from("pyproject.toml"),
        contents: render_pyproject(context),
    });
    files.push(GeneratedFile {
        relative_path: PathBuf::from("README.md"),
        contents: render_readme(context),
    });
    files.push(GeneratedFile {
        relative_path: package_dir.join("__init__.py"),
        contents: render_init_py(context),
    });
    files.push(GeneratedFile {
        relative_path: package_dir.join("common.py"),
        contents: render_common_py(context),
    });
    files.push(GeneratedFile {
        relative_path: package_dir.join("types.py"),
        contents: render_types_py(context),
    });
    files.push(GeneratedFile {
        relative_path: package_dir.join("catalog.py"),
        contents: render_catalog_py(),
    });
    for agent in context
        .abi
        .agents
        .iter()
        .filter(|agent| is_generated_user_agent(agent))
    {
        files.push(GeneratedFile {
            relative_path: package_dir.join(format!("{}.py", snake_case(&agent.name))),
            contents: render_agent_module(context, agent)?,
        });
    }
    Ok(files)
}

fn render_pyproject(context: &BindingContext) -> String {
    format!(
        r#"[build-system]
requires = ["setuptools>=68"]
build-backend = "setuptools.build_meta"

[project]
name = "{name}"
version = "0.0.1"
description = "Generated Corvid Python bindings for {source}"
requires-python = ">=3.9"
"#,
        name = context.package_name.replace('_', "-"),
        source = context.source_stem,
    )
}

fn render_readme(context: &BindingContext) -> String {
    format!(
        "# Generated Corvid Python bindings\n\nGenerated from `{source}`.\nDescriptor sha256: `{hash}`.\ncorvid-bind version: `{version}`.\nDescriptor generated_at: `{generated_at}`.\n",
        source = context.abi.source_path,
        hash = context.descriptor_hash_hex,
        version = context.bind_version,
        generated_at = context.abi.generated_at
    )
}

fn render_init_py(context: &BindingContext) -> String {
    let mut out = String::new();
    out.push_str("# @generated by corvid bind python.\n");
    out.push_str(&format!(
        "# descriptor sha256: {}\n",
        context.descriptor_hash_hex
    ));
    out.push_str(&format!("# corvid-bind version: {}\n", context.bind_version));
    out.push_str(&format!(
        "# descriptor generated_at: {}\n",
        context.abi.generated_at
    ));
    out.push_str("from .common import (\n");
    out.push_str("    ApprovalDecision,\n    ApprovalRequest,\n    Approver,\n    CallError,\n    Client,\n");
    out.push_str("    CorvidError,\n    DescriptorDriftError,\n    Grounded,\n    GroundedProvenance,\n    Observation,\n    TrustTier,\n    Weak,\n");
    out.push_str(")\n");
    out.push_str("from .catalog import AgentInfo, Catalog, CatalogQuery, Reversibility\n");
    out.push_str("from .types import *\n");
    for agent in context
        .abi
        .agents
        .iter()
        .filter(|agent| is_generated_user_agent(agent))
    {
        let module = snake_case(&agent.name);
        let marker = pascal_case(&agent.name);
        out.push_str(&format!("from .{module} import {marker}\n"));
    }
    out
}

